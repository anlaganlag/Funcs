# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, x):
#         self.val = x
#         self.left = None
#         self.right = None

class Solution:
    def distanceK(self, root: TreeNode, target: TreeNode, K: int) -> List[int]:
        graph = collections.defaultdict(set)
        def dfs(root):
            if root.left :
                graph[root.val].add(root.left.val)
                graph[root.left.val].add(root.val)
                dfs(root.left)
            if root.right:
                graph[root.val].add(root.right.val)
                graph[root.right.val].add(root.val)
                dfs(root.right)
        dfs(root)#构建图...
        q = [target.val]
        visited ={target.val}
        while K:
            nq = []
            while q:
                tmp = q.pop()
                for n in graph[tmp]:
                    if n not in visited:
                        visited.add(n)
                        nq.append(n)
            K -= 1
            q = nq
        return q
